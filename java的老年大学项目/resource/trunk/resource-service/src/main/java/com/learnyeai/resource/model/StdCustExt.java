package com.learnyeai.resource.model;

import com.learnyeai.learnai.support.BaseEntity;
import javax.persistence.Column;
import javax.persistence.Id;
import java.util.Date;

/**
 * 学习行为用户扩展
 *
 * @author yl
 */
public class StdCustExt extends BaseEntity {

    /**
    * 用户id
    */
    @Id
    @Column(name = "CUST_ID")
    private String custId;

    /**
     * 学习时长
     */
    @Column(name = "STUDY_TIME")
    private Long studyTime;
    /**
     * 学习次数
     */
    @Column(name = "STUDY_NUM")
    private Long studyNum;
    /**
     * 当前课程id
     */
    @Column(name = "CUR_COURSE_ID")
    private String curCourseId;
    /**
     * 当前章节id
     */
    @Column(name = "CUR_CHAPTER_ID")
    private String curChapterId;
    /**
     * 当前资源id
     */
    @Column(name = "CUR_RESOURCE_ID")
    private String curResourceId;
    /**
     * 学习断点
     */
    @Column(name = "STUDY_BREAKPOINT")
    private Long studyBreakpoint;
    /**
     * 创建时间
     */
    @Column(name = "CREATE_DATE")
    private Date createDate;
    /**
     * 更新时间
     */
    @Column(name = "UPDATE_DATE")
    private Date updateDate;
    /**
     * 站点
     */
    @Column(name = "SITE_ID")
    private String siteId;
    /**
     * 商户id
     */
    @Column(name = "MCHT_ID")
    private String mchtId;
    /**
     * 商户方案id
     */
    @Column(name = "MCHT_SCHM_ID")
    private String mchtSchmId;

    public String getCustId() {
        return custId;
    }

    public void setCustId(String custId) {
        this.custId = custId;
    }
    public Long getStudyTime() {
        return studyTime;
    }

    public void setStudyTime(Long studyTime) {
        this.studyTime = studyTime;
    }
    public Long getStudyNum() {
        return studyNum;
    }

    public void setStudyNum(Long studyNum) {
        this.studyNum = studyNum;
    }
    public String getCurCourseId() {
        return curCourseId;
    }

    public void setCurCourseId(String curCourseId) {
        this.curCourseId = curCourseId;
    }
    public String getCurChapterId() {
        return curChapterId;
    }

    public void setCurChapterId(String curChapterId) {
        this.curChapterId = curChapterId;
    }
    public String getCurResourceId() {
        return curResourceId;
    }

    public void setCurResourceId(String curResourceId) {
        this.curResourceId = curResourceId;
    }
    public Long getStudyBreakpoint() {
        return studyBreakpoint;
    }

    public void setStudyBreakpoint(Long studyBreakpoint) {
        this.studyBreakpoint = studyBreakpoint;
    }
    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }
    public Date getUpdateDate() {
        return updateDate;
    }

    public void setUpdateDate(Date updateDate) {
        this.updateDate = updateDate;
    }
    public String getSiteId() {
        return siteId;
    }

    public void setSiteId(String siteId) {
        this.siteId = siteId;
    }
    public String getMchtId() {
        return mchtId;
    }

    public void setMchtId(String mchtId) {
        this.mchtId = mchtId;
    }
    public String getMchtSchmId() {
        return mchtSchmId;
    }

    public void setMchtSchmId(String mchtSchmId) {
        this.mchtSchmId = mchtSchmId;
    }

    public static class TF {

        public static String TABLE_NAME = "STD_CUST_EXT";   // 表名

        //public static String TABLE_SCHEMA = ConfigUtils.getValue("");   // 库名

        public static String custId = "CUST_ID";  // 用户id
        public static String studyTime = "STUDY_TIME";  // 学习时长
        public static String studyNum = "STUDY_NUM";  // 学习次数
        public static String curCourseId = "CUR_COURSE_ID";  // 当前课程id
        public static String curChapterId = "CUR_CHAPTER_ID";  // 当前章节id
        public static String curResourceId = "CUR_RESOURCE_ID";  // 当前资源id
        public static String studyBreakpoint = "STUDY_BREAKPOINT";  // 学习断点
        public static String createDate = "CREATE_DATE";  // 创建时间
        public static String updateDate = "UPDATE_DATE";  // 更新时间
        public static String siteId = "SITE_ID";  // 站点
        public static String mchtId = "MCHT_ID";  // 商户id
        public static String mchtSchmId = "MCHT_SCHM_ID";  // 商户方案id

    }
}
