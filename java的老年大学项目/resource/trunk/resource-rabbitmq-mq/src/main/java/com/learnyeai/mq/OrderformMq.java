package com.learnyeai.mq;

import com.learnyeai.rabbitmq.bean.MqVo;

public class OrderformMq extends MqVo {

	private static final long serialVersionUID = -8887551716007996054L;

	public OrderformMq() {
	}

	public OrderformMq(String userId, String orderId, String orderNo, String czId, String batchId) {
		super();
		this.userId = userId;
		this.orderId = orderId;
		this.orderNo = orderNo;
		this.czId = czId;
		this.batchId = batchId;
	}

	private String userId;// 用户id
	private String orderId;// 订单id
	private String orderNo;// 订单号
	private String czId;// 班级id
	private String batchId;// 批次id

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getCzId() {
		return czId;
	}

	public void setCzId(String czId) {
		this.czId = czId;
	}

	public String getBatchId() {
		return batchId;
	}

	public void setBatchId(String batchId) {
		this.batchId = batchId;
	}

}
