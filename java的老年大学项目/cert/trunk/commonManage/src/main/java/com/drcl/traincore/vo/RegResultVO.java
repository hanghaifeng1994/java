package com.drcl.traincore.vo;

public class RegResultVO {
	private String userName;//可空 但是userName和IDNo至少有一个不为空
	private String IDNo;
	private String name;
	private String userId; //注册后的数据中心用户id，用户已经存在也要返回;
	private int result;//1成功 2 失败 不可空
	private String errorDesc;//失败描述 比如用户已存在，接口未开放等
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getIDNo() {
		return IDNo;
	}
	public void setIDNo(String iDNo) {
		IDNo = iDNo;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public int getResult() {
		return result;
	}
	public void setResult(int result) {
		this.result = result;
	}
	public String getErrorDesc() {
		return errorDesc;
	}
	public void setErrorDesc(String errorDesc) {
		this.errorDesc = errorDesc;
	}
	
}
