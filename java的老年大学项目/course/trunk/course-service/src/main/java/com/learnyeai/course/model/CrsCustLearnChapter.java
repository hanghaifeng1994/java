package com.learnyeai.course.model;

import com.learnyeai.learnai.support.BaseEntity;
import javax.persistence.Column;
import javax.persistence.Id;
import java.util.Date;

/**
 * 客户学习章节
 *
 * @author twang
 */
public class CrsCustLearnChapter extends BaseEntity {

    /**
    * id
    */
    @Id
    @Column(name = "LCPT_ID")
    private String lcptId;

    /**
     * 课程id
     */
    @Column(name = "CS_ID")
    private String csId;
    /**
     * 章节id
     */
    @Column(name = "CPT_ID")
    private String cptId;
    /**
     * 当前资源id
     */
    @Column(name = "CUR_RES_ID")
    private String curResId;
    /**
     * 是否完成
     */
    @Column(name = "STUDY_FINISHED")
    private String studyFinished;
    /**
     * 学习人id
     */
    @Column(name = "STUDY_USER_ID")
    private String studyUserId;
    /**
     * 创建时间
     */
    @Column(name = "CREATE_DATE")
    private Date createDate;
    /**
     * 更新时间
     */
    @Column(name = "UPDATE_DATE")
    private Date updateDate;
    /**
     * 站点
     */
    @Column(name = "SITE_ID")
    private String siteId;
    /**
     * 商户id
     */
    @Column(name = "MCHT_ID")
    private String mchtId;
    /**
     * 商户方案id
     */
    @Column(name = "MCHT_SCHM_ID")
    private String mchtSchmId;

    public String getLcptId() {
        return lcptId;
    }

    public void setLcptId(String lcptId) {
        this.lcptId = lcptId;
    }
    public String getCsId() {
        return csId;
    }

    public void setCsId(String csId) {
        this.csId = csId;
    }
    public String getCptId() {
        return cptId;
    }

    public void setCptId(String cptId) {
        this.cptId = cptId;
    }
    public String getCurResId() {
        return curResId;
    }

    public void setCurResId(String curResId) {
        this.curResId = curResId;
    }
    public String getStudyFinished() {
        return studyFinished;
    }

    public void setStudyFinished(String studyFinished) {
        this.studyFinished = studyFinished;
    }
    public String getStudyUserId() {
        return studyUserId;
    }

    public void setStudyUserId(String studyUserId) {
        this.studyUserId = studyUserId;
    }
    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }
    public Date getUpdateDate() {
        return updateDate;
    }

    public void setUpdateDate(Date updateDate) {
        this.updateDate = updateDate;
    }
    public String getSiteId() {
        return siteId;
    }

    public void setSiteId(String siteId) {
        this.siteId = siteId;
    }
    public String getMchtId() {
        return mchtId;
    }

    public void setMchtId(String mchtId) {
        this.mchtId = mchtId;
    }
    public String getMchtSchmId() {
        return mchtSchmId;
    }

    public void setMchtSchmId(String mchtSchmId) {
        this.mchtSchmId = mchtSchmId;
    }

    public static class TF {

        public static String TABLE_NAME = "CRS_CUST_LEARN_CHAPTER";   // 表名

        //public static String TABLE_SCHEMA = ConfigUtils.getValue("");   // 库名

        public static String lcptId = "LCPT_ID";  // id
        public static String csId = "CS_ID";  // 课程id
        public static String cptId = "CPT_ID";  // 章节id
        public static String curResId = "CUR_RES_ID";  // 当前资源id
        public static String studyFinished = "STUDY_FINISHED";  // 是否完成
        public static String studyUserId = "STUDY_USER_ID";  // 学习人id
        public static String createDate = "CREATE_DATE";  // 创建时间
        public static String updateDate = "UPDATE_DATE";  // 更新时间
        public static String siteId = "SITE_ID";  // 站点
        public static String mchtId = "MCHT_ID";  // 商户id
        public static String mchtSchmId = "MCHT_SCHM_ID";  // 商户方案id

    }
}
