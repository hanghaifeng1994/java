package com.learnyeai.base.api.vo;

import com.learnyeai.core.support.BaseVo;

/**
 * 订单功能包
 *
 * @author zhangpz
 */
public class ShOrderPackageVo extends BaseVo {

    /**
    * 订单功能包Id
    */
    private String ordPkgId;

    /**
     * 商户订单id
     */
    private String ordId;
    /**
     * 版本功能包历史id
     */
    private String edtPkgHisId;
    /**
     * 功能包名称
     */
    private String pkgName;

    public String getOrdPkgId() {
        return ordPkgId;
    }

    public void setOrdPkgId(String ordPkgId) {
        this.ordPkgId = ordPkgId;
    }
    public String getOrdId() {
        return ordId;
    }

    public void setOrdId(String ordId) {
        this.ordId = ordId;
    }
    public String getEdtPkgHisId() {
        return edtPkgHisId;
    }

    public void setEdtPkgHisId(String edtPkgHisId) {
        this.edtPkgHisId = edtPkgHisId;
    }
    public String getPkgName() {
        return pkgName;
    }

    public void setPkgName(String pkgName) {
        this.pkgName = pkgName;
    }

    public static class CF {
        public static String ordPkgId = "ordPkgId";  // 订单功能包Id
        public static String ordId = "ordId";  // 商户订单id
        public static String edtPkgHisId = "edtPkgHisId";  // 版本功能包历史id
        public static String pkgName = "pkgName";  // 功能包名称

    }

}
