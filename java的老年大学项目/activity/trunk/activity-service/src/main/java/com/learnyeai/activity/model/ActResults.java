package com.learnyeai.activity.model;

import com.learnyeai.learnai.support.BaseEntity;
import javax.persistence.Column;
import javax.persistence.Id;
import java.util.Date;

/**
 * 活动成果
 *
 * @author yl
 */
public class ActResults extends BaseEntity {

    /**
    * id
    */
    @Id
    @Column(name = "RST_ID")
    private String rstId;

    /**
     * 活动id
     */
    @Column(name = "ACT_ID")
    private String actId;
    /**
     * 标题
     */
    @Column(name = "RST_TITLE")
    private String rstTitle;
    /**
     * 内容
     */
    @Column(name = "RST_CONTENT")
    private String rstContent;
    /**
     * 封面
     */
    @Column(name = "RST_PHOTO")
    private String rstPhoto;
    /**
     * 附件ids
     */
    @Column(name = "RST_FILE_IDS")
    private String rstFileIds;
    /**
     * 附件名称
     */
    @Column(name = "RST_FILE_NAMES")
    private String rstFileNames;
    /**
     * 提交人id
     */
    @Column(name = "RST_USER_ID")
    private String rstUserId;
    /**
     * 提交人名称
     */
    @Column(name = "RST_USER_NAME")
    private String rstUserName;
    /**
     * 0未提交、1审核中、2审核通过、3审核不通过
     */
    @Column(name = "RST_STATUS")
    private String rstStatus;
    /**
     * 审核id
     */
    @Column(name = "RST_AUDIT_ID")
    private String rstAuditId;
    /**
     * 创建时间
     */
    @Column(name = "CREATE_DATE")
    private Date createDate;
    /**
     * 更新时间
     */
    @Column(name = "UPDATE_DATE")
    private Date updateDate;
    /**
     * 删除标记
     */
    @Column(name = "DEL_FLAG")
    private String delFlag;
    /**
     * 商户id
     */
    @Column(name = "MCHT_ID")
    private String mchtId;
    /**
     * 商户方案id
     */
    @Column(name = "MCHT_SCHM_ID")
    private String mchtSchmId;

    public String getRstId() {
        return rstId;
    }

    public void setRstId(String rstId) {
        this.rstId = rstId;
    }
    public String getActId() {
        return actId;
    }

    public void setActId(String actId) {
        this.actId = actId;
    }
    public String getRstTitle() {
        return rstTitle;
    }

    public void setRstTitle(String rstTitle) {
        this.rstTitle = rstTitle;
    }
    public String getRstContent() {
        return rstContent;
    }

    public void setRstContent(String rstContent) {
        this.rstContent = rstContent;
    }
    public String getRstPhoto() {
        return rstPhoto;
    }

    public void setRstPhoto(String rstPhoto) {
        this.rstPhoto = rstPhoto;
    }
    public String getRstFileIds() {
        return rstFileIds;
    }

    public void setRstFileIds(String rstFileIds) {
        this.rstFileIds = rstFileIds;
    }
    public String getRstFileNames() {
        return rstFileNames;
    }

    public void setRstFileNames(String rstFileNames) {
        this.rstFileNames = rstFileNames;
    }
    public String getRstUserId() {
        return rstUserId;
    }

    public void setRstUserId(String rstUserId) {
        this.rstUserId = rstUserId;
    }
    public String getRstUserName() {
        return rstUserName;
    }

    public void setRstUserName(String rstUserName) {
        this.rstUserName = rstUserName;
    }
    public String getRstStatus() {
        return rstStatus;
    }

    public void setRstStatus(String rstStatus) {
        this.rstStatus = rstStatus;
    }
    public String getRstAuditId() {
        return rstAuditId;
    }

    public void setRstAuditId(String rstAuditId) {
        this.rstAuditId = rstAuditId;
    }
    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }
    public Date getUpdateDate() {
        return updateDate;
    }

    public void setUpdateDate(Date updateDate) {
        this.updateDate = updateDate;
    }
    public String getDelFlag() {
        return delFlag;
    }

    public void setDelFlag(String delFlag) {
        this.delFlag = delFlag;
    }
    public String getMchtId() {
        return mchtId;
    }

    public void setMchtId(String mchtId) {
        this.mchtId = mchtId;
    }
    public String getMchtSchmId() {
        return mchtSchmId;
    }

    public void setMchtSchmId(String mchtSchmId) {
        this.mchtSchmId = mchtSchmId;
    }

    public static class TF {

        public static String TABLE_NAME = "ACT_RESULTS";   // 表名

        //public static String TABLE_SCHEMA = ConfigUtils.getValue("schema.interPlat");   // 库名

        public static String rstId = "RST_ID";  // id
        public static String actId = "ACT_ID";  // 活动id
        public static String rstTitle = "RST_TITLE";  // 标题
        public static String rstContent = "RST_CONTENT";  // 内容
        public static String rstPhoto = "RST_PHOTO";  // 封面
        public static String rstFileIds = "RST_FILE_IDS";  // 附件ids
        public static String rstFileNames = "RST_FILE_NAMES";  // 附件名称
        public static String rstUserId = "RST_USER_ID";  // 提交人id
        public static String rstUserName = "RST_USER_NAME";  // 提交人名称
        public static String rstStatus = "RST_STATUS";  // 0未提交、1审核中、2审核通过、3审核不通过
        public static String rstAuditId = "RST_AUDIT_ID";  // 审核id
        public static String createDate = "CREATE_DATE";  // 创建时间
        public static String updateDate = "UPDATE_DATE";  // 更新时间
        public static String delFlag = "DEL_FLAG";  // 删除标记
        public static String mchtId = "MCHT_ID";  // 商户id
        public static String mchtSchmId = "MCHT_SCHM_ID";  // 商户方案id

    }
}
