package com.drcl.traincore.vo;

public class StreetDataCountVO {
	private String name;
	private long streetNum;
	private long regNum;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getStreetNum() {
		return streetNum;
	}

	public void setStreetNum(long streetNum) {
		this.streetNum = streetNum;
	}

	public long getRegNum() {
		return regNum;
	}

	public void setRegNum(long regNum) {
		this.regNum = regNum;
	}

}
