        public static String content = "CONTENT";  // 内容
        public static String imgs = "IMGS";  // 图片
        public static String type = "TYPE";  // 业务类型
        public static String objId = "OBJ_ID";  // 对象id
        public static String status = "STATUS";  // 状态0未提交、1审核中、2审核通过、审核失败
        public static String pubStatus = "PUB_STATUS";  // 0未发布、1发布
        public static String auditId = "AUDIT_ID";  // 审核id
        public static String templateCode = "TEMPLATE_CODE";  // 模板编码
        public static String orgId = "ORG_ID";  // 接收机构id
        public static String orgName = "ORG_NAME";  // 接收机构名称
        public static String rcvUserIds = "RCV_USER_IDS";  // 接收人ids
        public static String createBy = "CREATE_BY";  // 创建人
        public static String createDate = "CREATE_DATE";  // 创建时间
        public static String updateBy = "UPDATE_BY";  // 更新人
        public static String updateDate = "UPDATE_DATE";  // 更新时间
        public static String delFlag = "DEL_FLAG";  // 删除标记
        public static String siteId = "SITE_ID";  // 站点
