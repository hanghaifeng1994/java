package com.drcl.traincore.vo;

public class ClazzStatisticsVO {

	private Long clazzId;//id
	private String clazzName;//班级名称
	private int studentsCount;//班级学员数
	private int youngCount;//青年学员数
	private int adultCount;//中年学员数
	private int oldCount;//老年学员数
	private int manCount;//男学员数
	private int womanCount;//女学员数
	
	public Long getClazzId() {
		return clazzId;
	}
	public void setClazzId(Long clazzId) {
		this.clazzId = clazzId;
	}
	
	public String getClazzName() {
		return clazzName;
	}
	public void setClazzName(String clazzName) {
		this.clazzName = clazzName;
	}
	public int getStudentsCount() {
		return studentsCount;
	}
	public void setStudentsCount(int studentsCount) {
		this.studentsCount = studentsCount;
	}
	public int getYoungCount() {
		return youngCount;
	}
	public void setYoungCount(int youngCount) {
		this.youngCount = youngCount;
	}
	public int getAdultCount() {
		return adultCount;
	}
	public void setAdultCount(int adultCount) {
		this.adultCount = adultCount;
	}
	public int getOldCount() {
		return oldCount;
	}
	public void setOldCount(int oldCount) {
		this.oldCount = oldCount;
	}
	public int getManCount() {
		return manCount;
	}
	public void setManCount(int manCount) {
		this.manCount = manCount;
	}
	public int getWomanCount() {
		return womanCount;
	}
	public void setWomanCount(int womanCount) {
		this.womanCount = womanCount;
	}	
}
