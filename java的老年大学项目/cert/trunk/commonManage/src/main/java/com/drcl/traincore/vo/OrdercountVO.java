package com.drcl.traincore.vo;

public class OrdercountVO
{
	private String source;      //来源
	private String name;		//行名
	private Long allCount;		//所有订单数
	private Long waitCount;		//待付订单数
	private Long novaildCount;	//失效订单数
	private Long finishedCount;	//完成订单数
	private Long refundCount;	//已退订单数
	private String sumprice;	//总金额
	private String onlineprice;	//在线支付金额
	private String offlineprice;//线下支付金额
	
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public Long getAllCount() {
		return allCount;
	}
	public void setAllCount(Long allCount) {
		this.allCount = allCount;
	}
	public Long getWaitCount() {
		return waitCount;
	}
	public void setWaitCount(Long waitCount) {
		this.waitCount = waitCount;
	}
	public Long getNovaildCount() {
		return novaildCount;
	}
	public void setNovaildCount(Long novaildCount) {
		this.novaildCount = novaildCount;
	}
	public Long getFinishedCount() {
		return finishedCount;
	}
	public void setFinishedCount(Long finishedCount) {
		this.finishedCount = finishedCount;
	}
	public Long getRefundCount() {
		return refundCount;
	}
	public void setRefundCount(Long refundCount) {
		this.refundCount = refundCount;
	}
	public String getSumprice() {
		return sumprice;
	}
	public void setSumprice(String sumprice) {
		this.sumprice = sumprice;
	}
	public String getOnlineprice() {
		return onlineprice;
	}
	public void setOnlineprice(String onlineprice) {
		this.onlineprice = onlineprice;
	}
	public String getOfflineprice() {
		return offlineprice;
	}
	public void setOfflineprice(String offlineprice) {
		this.offlineprice = offlineprice;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
    
}
