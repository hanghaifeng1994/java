package com.drcl.traincore.dto;

import java.io.Serializable;

public class BrandCategoryDTO implements Serializable {
	private static final long serialVersionUID = 229614430917452067L;

	private String brandCategoryId;

	private String brandId; // 品牌id

	private String categoryId;// 分类id

	public String getBrandCategoryId() {
		return brandCategoryId;
	}

	public void setBrandCategoryId(String brandCategoryId) {
		this.brandCategoryId = brandCategoryId;
	}

	public String getBrandId() {
		return brandId;
	}

	public void setBrandId(String brandId) {
		this.brandId = brandId;
	}

	public String getCategoryId() {
		return categoryId;
	}

	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}

}
