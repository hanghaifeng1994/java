package com.learnyeai.learnai.engine;

import java.io.Serializable;
import java.util.LinkedHashMap;
import java.util.Map;

/**
 * Created by zpz on 2016/4/19.
 */
public class RequestReport implements Serializable {
    private static final long serialVersionUID = 1496767848207954138L;

    private Map<String, Object> header = new LinkedHashMap();
    private Map<String, Object> payload = new LinkedHashMap();

    public RequestReport() {
    }

    public Map<String, Object> getHeader() {
        return this.header;
    }

    public void setHeader(Map<String, Object> header) {
        this.header = header;
    }

    public Map<String, Object> getPayload() {
        return this.payload;
    }

    public void setPayload(Map<String, Object> payload) {
        this.payload = payload;
    }
}
