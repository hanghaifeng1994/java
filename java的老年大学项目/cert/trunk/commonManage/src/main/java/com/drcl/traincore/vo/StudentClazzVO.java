package com.drcl.traincore.vo;

import java.util.List;

import com.google.common.collect.Lists;

public class StudentClazzVO {

	private Long id;
	
	private String username;
	
	private String idcard;
	
	private List<ClazzVO> clazzVOs=Lists.newArrayList();

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getIdcard() {
		return idcard;
	}

	public void setIdcard(String idcard) {
		this.idcard = idcard;
	}

	public List<ClazzVO> getClazzVOs() {
		return clazzVOs;
	}

	public void setClazzVOs(List<ClazzVO> clazzVOs) {
		this.clazzVOs = clazzVOs;
	}
}
