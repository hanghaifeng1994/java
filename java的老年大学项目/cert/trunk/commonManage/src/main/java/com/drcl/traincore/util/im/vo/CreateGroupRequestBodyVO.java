package com.drcl.traincore.util.im.vo;
/**
 * im接口创建主请求body
 * @author ycguo
 *   {
  "GP_NAME":"订单",
  "GP_PIC":"ddd",
  "GP_DESCRIPTION":"第三方对方",
  "PRJ_ID":"121212",
  "USER_ID":"121212"}}
 *
 */
public class CreateGroupRequestBodyVO {
	private String GP_NAME;//名称
	private String GP_PIC;//图片
	private String GP_DESCRIPTION;//描述
	private String PRJ_ID;//对应项目id
	private String USER_ID;//用户id

	public CreateGroupRequestBodyVO(String GP_NAME, String GP_PIC, String GP_DESCRIPTION,String PRJ_ID, String USER_ID){
		this.GP_NAME = GP_NAME;
		this.GP_PIC = GP_PIC;
		this.GP_DESCRIPTION = GP_DESCRIPTION;
		this.PRJ_ID =PRJ_ID;
		this.USER_ID = USER_ID;
	}

	public String getGP_NAME() {
		return GP_NAME;
	}

	public void setGP_NAME(String gPNAME) {
		GP_NAME = gPNAME;
	}

	public String getGP_PIC() {
		return GP_PIC;
	}

	public void setGP_PIC(String gPPIC) {
		GP_PIC = gPPIC;
	}

	public String getGP_DESCRIPTION() {
		return GP_DESCRIPTION;
	}

	public void setGP_DESCRIPTION(String gPDESCRIPTION) {
		GP_DESCRIPTION = gPDESCRIPTION;
	}

	public String getPRJ_ID() {
		return PRJ_ID;
	}

	public void setPRJ_ID(String pRJID) {
		PRJ_ID = pRJID;
	}

	public String getUSER_ID() {
		return USER_ID;
	}

	public void setUSER_ID(String uSERID) {
		USER_ID = uSERID;
	}
	
}
