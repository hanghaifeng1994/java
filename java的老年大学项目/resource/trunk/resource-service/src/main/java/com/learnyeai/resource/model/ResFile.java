package com.learnyeai.resource.model;

import com.learnyeai.learnai.support.BaseEntity;
import javax.persistence.Column;
import javax.persistence.Id;
import java.util.Date;

/**
 * 资源文件
 *
 * @author twang
 */
public class ResFile extends BaseEntity {

    /**
    * id
    */
    @Id
    @Column(name = "RES_FIEL_ID")
    private String resFielId;

    /**
     * 文件名
     */
    @Column(name = "FILE_NAME")
    private String fileName;
    /**
     * 资源id
     */
    @Column(name = "RES_ID")
    private String resId;
    /**
     * 文件id
     */
    @Column(name = "FILE_ID")
    private String fileId;
    /**
     * 文件类型
     */
    @Column(name = "FILE_TYPE")
    private String fileType;
    /**
     * 文件格式
     */
    @Column(name = "FILE_FORMAT")
    private String fileFormat;
    /**
     * 大小
     */
    @Column(name = "FILE_SIZE")
    private Long fileSize;
    /**
     * 后缀
     */
    @Column(name = "FILE_SUFFIX")
    private String fileSuffix;
    /**
     * 时长
     */
    @Column(name = "FILE_TIME_LEN")
    private Long fileTimeLen;
    /**
     * 下载次数
     */
    @Column(name = "DOWNLOAD_NUM")
    private Long downloadNum;
    /**
     * 创建人
     */
    @Column(name = "CREATE_BY")
    private String createBy;
    /**
     * 创建时间
     */
    @Column(name = "CREATE_DATE")
    private Date createDate;
    /**
     * 删除标记
     */
    @Column(name = "DEL_FLAG")
    private String delFlag;
    /**
     * 站点-m
     */
    @Column(name = "SITE_ID")
    private String siteId;
    /**
     * 商户id
     */
    @Column(name = "MCHT_ID")
    private String mchtId;
    /**
     * 商户方案id
     */
    @Column(name = "MCHT_SCHM_ID")
    private String mchtSchmId;

    public String getResFielId() {
        return resFielId;
    }

    public void setResFielId(String resFielId) {
        this.resFielId = resFielId;
    }
    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }
    public String getResId() {
        return resId;
    }

    public void setResId(String resId) {
        this.resId = resId;
    }
    public String getFileId() {
        return fileId;
    }

    public void setFileId(String fileId) {
        this.fileId = fileId;
    }
    public String getFileType() {
        return fileType;
    }

    public void setFileType(String fileType) {
        this.fileType = fileType;
    }
    public String getFileFormat() {
        return fileFormat;
    }

    public void setFileFormat(String fileFormat) {
        this.fileFormat = fileFormat;
    }
    public Long getFileSize() {
        return fileSize;
    }

    public void setFileSize(Long fileSize) {
        this.fileSize = fileSize;
    }
    public String getFileSuffix() {
        return fileSuffix;
    }

    public void setFileSuffix(String fileSuffix) {
        this.fileSuffix = fileSuffix;
    }
    public Long getFileTimeLen() {
        return fileTimeLen;
    }

    public void setFileTimeLen(Long fileTimeLen) {
        this.fileTimeLen = fileTimeLen;
    }
    public Long getDownloadNum() {
        return downloadNum;
    }

    public void setDownloadNum(Long downloadNum) {
        this.downloadNum = downloadNum;
    }
    public String getCreateBy() {
        return createBy;
    }

    public void setCreateBy(String createBy) {
        this.createBy = createBy;
    }
    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }
    public String getDelFlag() {
        return delFlag;
    }

    public void setDelFlag(String delFlag) {
        this.delFlag = delFlag;
    }
    public String getSiteId() {
        return siteId;
    }

    public void setSiteId(String siteId) {
        this.siteId = siteId;
    }
    public String getMchtId() {
        return mchtId;
    }

    public void setMchtId(String mchtId) {
        this.mchtId = mchtId;
    }
    public String getMchtSchmId() {
        return mchtSchmId;
    }

    public void setMchtSchmId(String mchtSchmId) {
        this.mchtSchmId = mchtSchmId;
    }

    public static class TF {

        public static String TABLE_NAME = "RES_FILE";   // 表名

        //public static String TABLE_SCHEMA = ConfigUtils.getValue("");   // 库名

        public static String resFielId = "RES_FIEL_ID";  // id
        public static String fileName = "FILE_NAME";  // 文件名
        public static String resId = "RES_ID";  // 资源id
        public static String fileId = "FILE_ID";  // 文件id
        public static String fileType = "FILE_TYPE";  // 文件类型
        public static String fileFormat = "FILE_FORMAT";  // 文件格式
        public static String fileSize = "FILE_SIZE";  // 大小
        public static String fileSuffix = "FILE_SUFFIX";  // 后缀
        public static String fileTimeLen = "FILE_TIME_LEN";  // 时长
        public static String downloadNum = "DOWNLOAD_NUM";  // 下载次数
        public static String createBy = "CREATE_BY";  // 创建人
        public static String createDate = "CREATE_DATE";  // 创建时间
        public static String delFlag = "DEL_FLAG";  // 删除标记
        public static String siteId = "SITE_ID";  // 站点-m
        public static String mchtId = "MCHT_ID";  // 商户id
        public static String mchtSchmId = "MCHT_SCHM_ID";  // 商户方案id

    }
}
