package com.drcl.traincore.vo;

public class EditUserVO {
	private String userId;// 数据中心用户id 不为空
	private String name;// 姓名 不可空
	private String email;// 邮箱 可空
	private String telephone;// 联系电话 可空
	private int genderCode;// 性别 1：男性 2女性 不可空
	private String mobile;// 手机 可空
	private String locationCode;// 区域编码
	private String qq;
	
	public EditUserVO() {
		super();
	}
	public EditUserVO(String userId, String name, String email, String telephone, int genderCode, String mobile, String locationCode) {
		super();
		this.userId = userId;
		this.name = name;
		this.email = email;
		this.telephone = telephone;
		this.genderCode = genderCode;
		this.mobile = mobile;
		this.locationCode = locationCode;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public int getGenderCode() {
		return genderCode;
	}

	public void setGenderCode(int genderCode) {
		this.genderCode = genderCode;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getLocationCode() {
		return locationCode;
	}

	public void setLocationCode(String locationCode) {
		this.locationCode = locationCode;
	}
	public String getQq() {
		return qq;
	}
	public void setQq(String qq) {
		this.qq = qq;
	}

}
