package com.learnyeai.audit.model;

import com.learnyeai.learnai.support.BaseEntity;

import javax.persistence.Column;
import javax.persistence.Id;
import java.util.Date;

/**
 * 上报审核
 *
 * @author yl
 */
public class AdtReportUp extends BaseEntity {

    /**
    * ID
    */
    @Id
    @Column(name = "RP_ID")
    private String rpId;

    /**
     * 标题
     */
    @Column(name = "RP_TITLE")
    private String rpTitle;
    /**
     * 对象id
     */
    @Column(name = "OBJ_ID")
    private String objId;
    /**
     * 业务类型
     */
    @Column(name = "SERVICE_TYPE")
    private String serviceType;
    /**
     * 详情url
     */
    @Column(name = "DETAIL_URL")
    private String detailUrl;
    /**
     * 0未审核、1审核通过、2审核未通过、3取消审核
     */
    @Column(name = "AUDIT_STATUS")
    private String auditStatus;
    /**
     * 审核人id
     */
    @Column(name = "AUDIT_USER_ID")
    private String auditUserId;
    /**
     * 审核人名称
     */
    @Column(name = "AUDIT_USER_NAME")
    private String auditUserName;
    /**
     * 审核意见
     */
    @Column(name = "AUDIT_CONTENT")
    private String auditContent;
    /**
     * 审核时间
     */
    @Column(name = "AUDIT_DATE")
    private Date auditDate;
    /**
     * 上报站点
     */
    @Column(name = "REPORT_SITE_ID")
    private String reportSiteId;
    /**
     * 上报站点名称
     */
    @Column(name = "REPORT_SITE_NAME")
    private String reportSiteName;
    /**
     * 平台id
     */
    @Column(name = "PT_ID")
    private String ptId;
    /**
     * 商户id
     */
    @Column(name = "MCHT_ID")
    private String mchtId;
    /**
     * 商户方案id
     */
    @Column(name = "MCHT_SCHM_ID")
    private String mchtSchmId;

    public String getRpId() {
        return rpId;
    }

    public void setRpId(String rpId) {
        this.rpId = rpId;
    }
    public String getRpTitle() {
        return rpTitle;
    }

    public void setRpTitle(String rpTitle) {
        this.rpTitle = rpTitle;
    }
    public String getObjId() {
        return objId;
    }

    public void setObjId(String objId) {
        this.objId = objId;
    }
    public String getServiceType() {
        return serviceType;
    }

    public void setServiceType(String serviceType) {
        this.serviceType = serviceType;
    }
    public String getDetailUrl() {
        return detailUrl;
    }

    public void setDetailUrl(String detailUrl) {
        this.detailUrl = detailUrl;
    }
    public String getAuditStatus() {
        return auditStatus;
    }

    public void setAuditStatus(String auditStatus) {
        this.auditStatus = auditStatus;
    }
    public String getAuditUserId() {
        return auditUserId;
    }

    public void setAuditUserId(String auditUserId) {
        this.auditUserId = auditUserId;
    }
    public String getAuditUserName() {
        return auditUserName;
    }

    public void setAuditUserName(String auditUserName) {
        this.auditUserName = auditUserName;
    }
    public String getAuditContent() {
        return auditContent;
    }

    public void setAuditContent(String auditContent) {
        this.auditContent = auditContent;
    }
    public Date getAuditDate() {
        return auditDate;
    }

    public void setAuditDate(Date auditDate) {
        this.auditDate = auditDate;
    }
    public String getReportSiteId() {
        return reportSiteId;
    }

    public void setReportSiteId(String reportSiteId) {
        this.reportSiteId = reportSiteId;
    }
    public String getReportSiteName() {
        return reportSiteName;
    }

    public void setReportSiteName(String reportSiteName) {
        this.reportSiteName = reportSiteName;
    }
    public String getPtId() {
        return ptId;
    }

    public void setPtId(String ptId) {
        this.ptId = ptId;
    }
    public String getMchtId() {
        return mchtId;
    }

    public void setMchtId(String mchtId) {
        this.mchtId = mchtId;
    }
    public String getMchtSchmId() {
        return mchtSchmId;
    }

    public void setMchtSchmId(String mchtSchmId) {
        this.mchtSchmId = mchtSchmId;
    }

    public static class TF {

        public static String TABLE_NAME = "ADT_REPORT_UP";   // 表名

        //public static String TABLE_SCHEMA = ConfigUtils.getValue("schema.interPlat");   // 库名

        public static String rpId = "RP_ID";  // ID
        public static String rpTitle = "RP_TITLE";  // 标题
        public static String objId = "OBJ_ID";  // 对象id
        public static String serviceType = "SERVICE_TYPE";  // 业务类型
        public static String detailUrl = "DETAIL_URL";  // 详情url
        public static String auditStatus = "AUDIT_STATUS";  // 0未审核、1审核通过、2审核未通过、3取消审核
        public static String auditUserId = "AUDIT_USER_ID";  // 审核人id
        public static String auditUserName = "AUDIT_USER_NAME";  // 审核人名称
        public static String auditContent = "AUDIT_CONTENT";  // 审核意见
        public static String auditDate = "AUDIT_DATE";  // 审核时间
        public static String reportSiteId = "REPORT_SITE_ID";  // 上报站点
        public static String reportSiteName = "REPORT_SITE_NAME";  // 上报站点名称
        public static String ptId = "PT_ID";  // 平台id
        public static String mchtId = "MCHT_ID";  // 商户id
        public static String mchtSchmId = "MCHT_SCHM_ID";  // 商户方案id

    }
}
